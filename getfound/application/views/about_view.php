	Getfound.ly is a privately-founded, privately-funded startup based out of Peoria, Illinois.
	
	<br><br>
	
	<font face="georgia" style='font-size:18pt;'><b>What's the big idea?</b></font>
	
	<br><br>
	
	Simply put, we felt like most business card-esque sites were missing one key thing and that is the ability to find a user easily.  The problem with most of these sites is the ability to choose any extention you want.  I know people who have something like the following: "link.com/iplaybassinabandandimreallygood".  Now if I am trying to remember that address based completely on memory from the person saying it once, I'm going to have a hard time.
	
	<br><br>
	
	With Getfound.ly we've made this more simple.  We take a user's first and last initials and add an integer to the end.  This becomes their username and the #gfid for which people will be able to easily find them.  "getfound.ly/dw001" is a little bit easier to remember if you ask us.
	
	<br><br>
	
	Additionally, we believe that your #gfid can be used for a large amount of other uses.  One of those uses we've come up with is a 'lost and found' type application.  Lets say you lost your backpack while on a trip to Chicago.  Inside the bag you have written "#gfid:dw001".  The person who finds your bag can now easily visit Getfound.ly, type in your #gfid, and have direct links to your facebook, linkedin, twitter, or about.me pages in order to get your bag back to you.
	
	<br><br>
	
	We believe Getfound.ly is the type of online business card website the web has been looking for.  Easily intermixed with several social networking mediums, and a uniform layout that is both pleasant on the eyes and on the mind.
	
